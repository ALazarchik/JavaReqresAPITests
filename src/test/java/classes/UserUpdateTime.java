package classes;

public class UserUpdateTime {
    private String name;
    private String job;

    public UserUpdateTime() {
    }

    public UserUpdateTime(String name, String job) {
        this.name = name;
        this.job = job;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
